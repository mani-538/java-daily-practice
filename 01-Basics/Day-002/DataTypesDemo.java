public class DataTypesDemo {

    public static void main(String[] args) {

        byte age = 18;
        short year = 2026;
        int population = 150000;
        long mobile = 9876543210L;

        float height = 5.8f;
        double salary = 50000.75;

        char grade = 'A';

        boolean passed = true;

        System.out.println("Byte : " + age);
        System.out.println("Short : " + year);
        System.out.println("Int : " + population);
        System.out.println("Long : " + mobile);

        System.out.println("Float : " + height);
        System.out.println("Double : " + salary);

        System.out.println("Char : " + grade);

        System.out.println("Boolean : " + passed);

    }

}